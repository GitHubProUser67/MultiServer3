    /// <summary>
    /// Tries to load the specified configuration file.
    /// Throws an exception if it fails to find the file.
    /// </summary>
    /// <param name="configPath"></param>
    /// <exception cref="FileNotFoundException"></exception>
    public static void RefreshVariables(string configPath)
    {
        // Make sure the file exists
        if (!File.Exists(configPath))
        {
            LoggerAccessor.LogWarn("Could not find the multispy.json file, writing and using server's default.");

            Directory.CreateDirectory(Path.GetDirectoryName(configPath) ?? Directory.GetCurrentDirectory() + "/static");

            // Write the JObject to a file
            File.WriteAllText(configPath, new JObject(
                new JProperty("enable_login", EnableLogin),
                new JProperty("enable_peer_chat", EnablePeerChat),
                new JProperty("enable_natneg", EnableNatNeg),
                new JProperty("enable_cdkey", EnableCdKey),
                new JProperty("enable_master", EnableMaster),
                new JProperty("enable_list", EnableList),
                SerializeGamesKey()
            ).ToString());

            return;
        }

        try
        {
            // Parse the JSON configuration
            dynamic config = JObject.Parse(File.ReadAllText(configPath));

            EnableLogin = GetValueOrDefault(config, "enable_login", EnableLogin);
            EnablePeerChat = GetValueOrDefault(config, "enable_peer_chat", EnablePeerChat);
            EnableNatNeg = GetValueOrDefault(config, "enable_natneg", EnableNatNeg);
            EnableCdKey = GetValueOrDefault(config, "enable_cdkey", EnableCdKey);
            EnableMaster = GetValueOrDefault(config, "enable_master", EnableMaster);
            EnableMaster = GetValueOrDefault(config, "enable_master", EnableMaster);
            DatabasePath = GetValueOrDefault(config, "database_path", DatabasePath);
            GamesKey = GetValueOrDefault(config, "games_key", GamesKey);
        }
        catch (Exception ex)
        {
            LoggerAccessor.LogWarn($"multispy.json file is malformed (exception: {ex}), using server's default.");
        }
    }

    // Helper method to get a value or default value if not present
    public static T GetValueOrDefault<T>(dynamic obj, string propertyName, T defaultValue)
    {
        try
        {
            if (obj != null)
            {
                if (obj is JObject jObject)
                {
                    if (jObject.TryGetValue(propertyName, out JToken? value))
                    {
                        T? returnvalue = value.ToObject<T>();
                        if (returnvalue != null)
                            return returnvalue;
                    }
                }
            }
        }
        catch (Exception ex)
        {
            LoggerAccessor.LogError($"[Program] - GetValueOrDefault thrown an exception: {ex}");
        }

        return defaultValue;
    }

    // Helper method for the GamesKey config serialization.
    private static JProperty SerializeGamesKey()
    {
        JObject jObject = new();
        foreach (var kvp in GamesKey ?? new Dictionary<string, string>())
        {
            jObject.Add(kvp.Key, kvp.Value);
        }
        return new JProperty("games_key", jObject);
    }

    // http://aluigi.altervista.org/papers/gslist.cfg
    public static string GetKeyFromGameName(string gamename)
    {
        if (GamesKey.TryGetValue(gamename, out string? value) && !string.IsNullOrEmpty(value))
            return value;

        return "Xn221z"; // Unihandled default value.
    }
